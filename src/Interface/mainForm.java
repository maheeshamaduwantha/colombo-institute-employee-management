    private void txtSearch1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearch1KeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            try{
                String sql = "select * from `employee_list` where `epfNo` = ?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, txtSearch1.getText());
                rs = ps.executeQuery();
                if (rs.next()) {
                    txtFname2.setText(rs.getString(2));
                    txtLname2.setText(rs.getString(3));
                    txtDepartment2.setText(rs.getString(4));
                    txtDesignation2.setText(rs.getString(5));
                    txtNumber2.setText(rs.getString(6));
                    if (rs.getString(7).equals("Active")) {
                        r_Active2.setSelected(true);
                        r_Inactive2.setSelected(false);
                    } else if (rs.getString(7).equals("In - Active")){
                        r_Active2.setSelected(false);
                        r_Inactive2.setSelected(true);
                        
                    }
                    txtAddress2.setText(rs.getString(8));
                }
            } catch (Exception ex){
                    JOptionPane.showConfirmDialog(null, ex);
            }  
        }
        
    }//GEN-LAST:event_txtSearch1KeyPressed

